def reverse_norm(im):
    max_ = np.max(im)
    min_ = np.percentile(im, 0)
    # min_ = np.min(im)
    im = np.clip(im, min_, max_)
    im = (im - min_) / (max_ - min_) * 65535
    return im.astype(np.uint16)


def add_poisson(img, poisson):
    img = poisson * img
    img = np.random.poisson(img.astype(np.int)).astype(np.float32)
    return img


def add_gauss_noise(x, gauss_sigma, gauss_mean=0.0):
    noise = np.random.normal(gauss_mean, gauss_sigma, size=x.shape).astype(np.float32)
    x = np.maximum(0, x + noise)
    return x


def generate_LR(img, scale=6, scale_order_list=[0, 1, 3], gause_noise_list=[0.01, 0.05, 0.01],
                possion_noise_list=[10000, 20000, 10000]):
    scale_order = int(random.choice(scale_order_list))
    sd = 1 / scale
    x_scale = zoom(img, [sd, sd, sd, 1], order=scale_order)
    gl = np.arange(gause_noise_list[0], gause_noise_list[1], gause_noise_list[2])
    gauss_noise = float(random.choice(gl))
    x_gauss = add_gauss_noise(x_scale, gauss_noise, 0)
    x_gauss = normalize_percentile(x_gauss, 0, 100)
    pl = np.arange(possion_noise_list[0], possion_noise_list[1], possion_noise_list[2])
    poisson_noise = float(random.choice(pl))
    x_poisson = add_poisson(x_gauss, poisson_noise)
    x = normalize_percentile(x_poisson, 0, 100)
    return x


