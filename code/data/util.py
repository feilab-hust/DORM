def augment(img_list, hflip=True, rot=True):
    # horizontal flip OR rotate
    hflip = hflip and random.random() < 0.5
    vflip = rot and random.random() < 0.5
    rot90 = rot and random.random() < 0.5
    size = len(img_list[0].shape)

    def _augment(img):
        if size == 5:
            if hflip:
                img = img[:, :, :, :, ::-1]
            if vflip:
                img = img[:, :, :, ::-1, :]
            if rot90:
                img = img.transpose(0, 1, 2, 4, 3)
            return img
        elif size == 4:
            if hflip:
                img = img[:, :, :, ::-1]
            if vflip:
                img = img[:, :, ::-1, :]
            if rot90:
                img = img.transpose(0, 1, 3, 2)
            return img

    return [_augment(img) for img in img_list]


def augment_flow(img_list, flow_list, hflip=True, rot=True):
    # horizontal flip OR rotate
    hflip = hflip and random.random() < 0.5
    vflip = rot and random.random() < 0.5
    rot90 = rot and random.random() < 0.5

    def _augment(img):
        if hflip:
            img = img[:, :, ::-1, :]
        if vflip:
            img = img[:, ::-1, :, :]
        if rot90:
            img = img.transpose(0, 2, 1, 3)
        return img

    def _augment_flow(flow):
        if hflip:
            flow = flow[:, :, ::-1, :]
            flow[:, :, :, 0] *= -1
        if vflip:
            flow = flow[:, ::-1, :, :]
            flow[:, :, :, 1] *= -1
        if rot90:
            flow = flow.transpose(0, 2, 1, 3)
            flow = flow[:, :, :, [1, 0]]
        return flow

    rlt_img_list = [_augment(img) for img in img_list]
    rlt_flow_list = [_augment_flow(flow) for flow in flow_list]

    return rlt_img_list, rlt_flow_list


def modcrop(img_in, scale):
    # img_in: Numpy, HWC or HW
    img = np.copy(img_in)
    if img.ndim == 3:
        D, H, W = img.shape
        D_r, H_r, W_r = D % scale, H % scale, W % scale
        img = img[:D - D_r, :H - H_r, :W - W_r]
    elif img.ndim == 4:
        D, H, W, C = img.shape
        D_r, H_r, W_r = D % scale, H % scale, W % scale
        img = img[:D - D_r, :H - H_r, :W - W_r, :]
    else:
        raise ValueError('Wrong img ndim: [{:d}].'.format(img.ndim))
    return img


